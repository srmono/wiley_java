package basics;

public class Operators {
	public static void main(String[] args) {
		
		// Arithmetic operators: 
		// These operators are used to perform arithmetic operations on numeric data types, including 
//		addition (+), 
//		subtraction (-), 
//		multiplication (*), 
//		division (/), and 
//		modulus (%). For example:
//		int x = 10;
//		int y = 5;
//		int sum = x + y; // sum is 15
//		int diff = x - y; // diff is 5
//		int product = x * y; // product is 50
//		int quotient = x / y; // quotient is 2
//		int remainder = x % y; // remainder is 0
		
		// Relational operators: These operators are used to compare two values and return a boolean value (true or false), including less than (<), greater than (>), less than or equal to (<=), greater than or equal to (>=), equal to (==), and not equal to (!=). For example:
//		int x = 10;
//		int y = 5;
//		boolean isGreaterThan = x > y; // true
//		boolean isEqualTo = x == y; // false
//		boolean isLessThanOrEqualTo = x <= y; // false
		
		
		// Logical operators: These operators are used to combine two or more boolean expressions and return a boolean value, including logical AND (&&), logical OR (||), and logical NOT (!). For example:
//		int x = 10;
//		int y = 5;
//		boolean isGreater = x > y;
//		boolean isPositive = x > 0;
//		boolean result = isGreater && isPositive; // true

		
		//Bitwise operators: These operators are used to perform bitwise operations on integer data types, including bitwise AND (&), bitwise OR (|), bitwise XOR (^), bitwise complement (~), left shift (<<), and right shift (>>). For example:
//		int x = 10;
//		int y = 5;
//		int result1 = x & y; // 0
//		int result2 = x | y; // 15
//		int result3 = x ^ y; // 15
//		int result4 = ~x; // -11
//		int result5 = x << 1; // 20
//		int result6 = x >> 1; // 5

		// Assignment operators: These operators are used to assign values to variables, including simple assignment (=), addition and assignment (+=), subtraction and assignment (-=), multiplication and assignment (*=), division and assignment (/=), and modulus and assignment (%=). For example:
		int x = 10;
		x += 5; // equivalent to x = x + 5;
		x -= 3; // equivalent to x = x - 3;
		x *= 2; // equivalent to x = x * 2;
		x /= 4; // equivalent to x = x / 4;
		x %= 2; // equivalent to x = x % 2;

		


	}
}
